void main() {
  int i;
  for (i = 1; i <= 10; i++) {
    if (i % 2 == 0) {
      print(i);
    }
  }
  for (i = 2; i <= 10; i += 2) {
    print(i);
  }
  // Using while loop
  i = 1;
  while (i <= 10) {
    if (i % 2 == 0) {
      print(i);
    }
    i++;
  }
}

